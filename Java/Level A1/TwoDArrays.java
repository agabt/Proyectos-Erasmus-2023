public class TwoDArrays {
    public static void main(String[] args) {
        String[][] drawings = { { " ", "(", " ", "*", ">" }, { "(", " ", ">", " ", ")" }, { " ", " ", ">", ">", " " } };

        for (int i = 0; i < drawings.length; i++) {
            System.out.println(drawings[i]);
        }
    }
}
